def output(msg, level=0):
	if level == 0: # info
		print("[i] " + msg)
	elif level == 1: # success
		print("\033[32m[+]\033[0m " + msg)
	elif level == 2: # warning
		print("\033[31m[-]\033[0m " + msg)

def send_request(url):
	#output("Sending: %s"%url, 0)
	response = requests.get(url)
	if response.status_code == 200:
		output("%d: %s" % (response.status_code, url), 1)
	else:
		output("%d: %s" % (response.status_code, url), 2)
	return response
